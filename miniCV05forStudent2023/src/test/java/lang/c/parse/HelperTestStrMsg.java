package lang.c.parse;

public class HelperTestStrMsg {
    private String testStr;
    private String msg;
    public HelperTestStrMsg(String str, String msg) {
        this.testStr = str;
        this.msg = msg;
    }
    public String getTestStr() {
        return testStr;
    }
    public String getMsg() {
        return msg;
    }
}